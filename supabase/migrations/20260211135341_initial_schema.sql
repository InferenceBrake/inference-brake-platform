-- ============================================
-- USERS TABLE
-- ============================================
CREATE TABLE users (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  email TEXT UNIQUE NOT NULL,
  api_key TEXT UNIQUE NOT NULL,
  plan TEXT NOT NULL DEFAULT 'hobby', -- hobby, pro, enterprise
  daily_limit INTEGER NOT NULL DEFAULT 1000,
  checks_today INTEGER NOT NULL DEFAULT 0,
  webhook_url TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Create the increment function
CREATE OR REPLACE FUNCTION increment_usage(user_uuid UUID)
RETURNS void AS $$
BEGIN
  UPDATE users 
  SET checks_today = checks_today + 1 
  WHERE id = user_uuid;
END;
$$ LANGUAGE plpgsql;

-- Grant permission so the Edge Function (using service_role) can call it
GRANT EXECUTE ON FUNCTION increment_usage(UUID) TO service_role;

-- Reset daily counters at midnight (UTC)
CREATE OR REPLACE FUNCTION reset_daily_checks()
RETURNS void AS $$
BEGIN
  UPDATE users SET checks_today = 0;
END;
$$ LANGUAGE plpgsql;

