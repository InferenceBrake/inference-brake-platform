-- Index for fast API key lookup
CREATE INDEX idx_users_api_key ON users(api_key);

-- ============================================
-- REASONING HISTORY TABLE
-- ============================================
CREATE TABLE reasoning_history (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  session_id TEXT NOT NULL,
  user_id UUID NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  step_number INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  reasoning TEXT NOT NULL,
  action TEXT,  -- Extracted action for repetition detection
  embedding vector(384), -- gte-small 384 dims, openai ai-embeddings-v1 1536 dimensions
  similarity FLOAT,
  loop_detected BOOLEAN NOT NULL DEFAULT FALSE,
  metadata JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Indexes for performance
CREATE INDEX idx_reasoning_session ON reasoning_history(session_id, step_number);
CREATE INDEX idx_reasoning_user ON reasoning_history(user_id);
CREATE INDEX idx_reasoning_created ON reasoning_history(created_at);

-- Auto-delete old history (keep last 30 days)
CREATE OR REPLACE FUNCTION delete_old_history()
RETURNS void AS $$
BEGIN
  DELETE FROM reasoning_history 
  WHERE created_at < NOW() - INTERVAL '30 days';
END;
$$ LANGUAGE plpgsql;

-- ============================================
-- METRICS TABLE (for analytics)
-- ============================================
CREATE TABLE metrics (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  session_id TEXT NOT NULL,
  loop_detected BOOLEAN NOT NULL,
  similarity FLOAT,
  estimated_cost_saved FLOAT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_metrics_user ON metrics(user_id);
CREATE INDEX idx_metrics_created ON metrics(created_at);

-- ============================================
-- ALERTS TABLE
-- ============================================
CREATE TABLE alerts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  session_id TEXT NOT NULL,
  severity TEXT NOT NULL, -- warning, danger
  message TEXT NOT NULL,
  acknowledged BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_alerts_user ON alerts(user_id, acknowledged);

