-- Generate API key
CREATE OR REPLACE FUNCTION generate_api_key()
RETURNS TEXT AS $$
BEGIN
  RETURN 'ib_' || encode(gen_random_bytes(32), 'hex');
END;
$$ LANGUAGE plpgsql;

-- Create new user with API key
CREATE OR REPLACE FUNCTION create_user(
  user_email TEXT,
  user_plan TEXT DEFAULT 'hobby'
)
RETURNS TABLE(id UUID, email TEXT, api_key TEXT) AS $$
DECLARE
  new_api_key TEXT;
  new_user_id UUID;
BEGIN
  new_api_key := generate_api_key();
  
  INSERT INTO users (email, api_key, plan, daily_limit)
  VALUES (
    user_email,
    new_api_key,
    user_plan,
    CASE
      WHEN user_plan = 'hobby' THEN 1000
      WHEN user_plan = 'pro' THEN 10000
      ELSE 999999
    END
  )
  RETURNING users.id INTO new_user_id;
  
  RETURN QUERY SELECT new_user_id, user_email, new_api_key;
END;
$$ LANGUAGE plpgsql;

-- Get user stats
CREATE OR REPLACE FUNCTION get_user_stats(user_uuid UUID)
RETURNS TABLE(
  total_checks BIGINT,
  loops_detected BIGINT,
  total_savings FLOAT,
  avg_similarity FLOAT
) AS $$
BEGIN
  RETURN QUERY
  SELECT
    COUNT(*)::BIGINT as total_checks,
    COUNT(*) FILTER (WHERE loop_detected = true)::BIGINT as loops_detected,
    COALESCE(SUM(estimated_cost_saved), 0)::FLOAT as total_savings,
    COALESCE(AVG(similarity), 0)::FLOAT as avg_similarity
  FROM metrics
  WHERE user_id = user_uuid;
END;
$$ LANGUAGE plpgsql;

-- ============================================
-- ANALYTICS VIEWS
-- ============================================

CREATE OR REPLACE VIEW daily_stats AS
SELECT
  DATE(created_at) as date,
  user_id,
  COUNT(*) as total_checks,
  COUNT(*) FILTER (WHERE loop_detected = true) as loops_detected,
  ROUND(AVG(similarity)::numeric, 3) as avg_similarity,
  ROUND(SUM(estimated_cost_saved)::numeric, 2) as total_savings
FROM metrics
GROUP BY DATE(created_at), user_id
ORDER BY date DESC;

